class Solution {
  public:
    int canReach(int A[], int N) {
        int i = 0;
        int maxreach = 0;
        
        while (i<N and i<=maxreach){
            maxreach = max(maxreach,i+A[i]);
            i++;
        }
        return i==N;
    }
};

